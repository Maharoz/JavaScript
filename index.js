// Logical AND (&&)
//returns true if both operands are true
let highIncome = true;
let goodCreditScore = true;
let eligibleForLoan = highIncome && goodCreditScore;

console.log(eligibleForLoan);

// Logical OR (||)
//returns true if both operands are true


let highIncome = true;
let goodCreditScore = false;
let eligibleForLoan = highIncome || goodCreditScore;

console.log(eligibleForLoan);


//Logical NOT (!)

let applicationRefused = !eligibleForLoan;

console.log(eligibleForLoan);

